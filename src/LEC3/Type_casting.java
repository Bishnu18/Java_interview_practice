package LEC3;

public class Type_casting {
    public static void main(String[] args) {
        int a=045;
        System.out.println(a);
        byte b=(byte) 128;
        byte b1=(byte)140;
        byte b2=(byte)300;
        byte a1=12;
        System.out.println(b);
        System.out.println(b1);
        System.out.println(b2);
        System.out.println(a1);
    }
}
